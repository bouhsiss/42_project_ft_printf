#include"ft_printf.h"

int	ft_putnbr_u(unsigned int n)
{
	int	i;

	i = 0;
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	else
	{
		n += '0';
		i += ft_putchar(n);
	}
	return (i);
}
